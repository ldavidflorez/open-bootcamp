package com.openbootcamptest;

public class DataTypes {
    public static void main(String[] args) {
        /*
            Primitives data types
        */

        // integers
        byte number1 = 1; // 1 byte
        short number2 = 2; // 2 bytes
        int number3 = 3; // 4 bytes
        long number4 = 4; // 8 bytes

        // float point
        float float1 = 1.5f;
        double float2 = 3.1416d; // more precision (capacity)

        // character
        char character = 'L';

        // booleans
        boolean bool1 = true;
        boolean bool2 = false;

        /*
            Wrappers data types (classes)
        */

        // strings
        String name = "Luis";
        String lastname = "Louis";

        // integer
        Integer number5 = 10;
    }
}
